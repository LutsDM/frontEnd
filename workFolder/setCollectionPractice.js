/*Учебная задача: Управление участниками мероприятия
Условие
Вы организатор мероприятия и хотите вести список участников. Однако каждому участнику разрешается зарегистрироваться только один раз. Для этого вы решаете использовать Set в JavaScript.

Функционал, который нужно реализовать:

Создайте пустой Set для хранения имен участников.

Напишите функции для следующих действий:
addParticipant(name): добавляет участника в список. Если участник уже зарегистрирован, выводит сообщение: "Участник [name] уже зарегистрирован".
removeParticipant(name): удаляет участника из списка. Если участника нет, выводит сообщение: "Участник [name] не найден".
listParticipants(): выводит список всех участников в консоль.
После выполнения нескольких операций посчитайте общее количество участников.

Дополнительные требования:
Реализуйте проверку, чтобы имена участников не были пустыми строками. Если имя пустое, выводите ошибку: "Имя не может быть пустым".
Напишите функцию clearParticipants(), которая очищает весь список участников.
Напишите пример использования всех функций: добавьте несколько участников, удалите одного, выведите список и общее количество.

Пример работы программы
javascript
Копировать код
addParticipant("Alice"); // Участник Alice добавлен
addParticipant("Bob"); // Участник Bob добавлен
addParticipant("Alice"); // Участник Alice уже зарегистрирован
listParticipants(); 
// Список участников:
// Alice
// Bob
removeParticipant("Bob"); // Участник Bob удален
removeParticipant("Tom"); // Участник Tom не найден

clearParticipants(); 
listParticipants(); // Список участников пуст
Эта задача помогает понять:

Как добавлять и удалять элементы в Set.
Как проверять наличие элемента.
Как перебирать элементы Set с использованием итераторов.*/

const listParticipants = new Set();
function addParticipant (name) {
    const originSize = listParticipants.size
    listParticipants.add(name)
    if (originSize === listParticipants.size) { 
        console.log(`Участник "${name}" уже зарегистрирован`);
    } else {
        console.log(`Участник "${name}" добавлен`);
    }
}

addParticipant("Alice");
addParticipant("Alice");
addParticipant("Bob");

console.log(listParticipants)